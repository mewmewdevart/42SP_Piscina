char	*ft_strcpy(char *dest, char *src)
{
	int	lis;

	lis = 0;
	while (src[lis] != '\0')
	{
		dest[lis] = src[lis];
		lis++;
	}
	dest[lis] = '\0';
	return (dest);
}
