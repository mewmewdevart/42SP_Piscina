#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	c;
	int	d;

	c = 0;
	d = 1;
	if (argc > 1)
	{
		while (argc - d >= 1)
		{
			c = 0;
			while (argv[d][c] != '\0')
			{
				write (1, &argv[d][c], 1);
				c++;
			}
			write (1, "\n", 1);
			d++;
		}
	}
	return (0);
}
