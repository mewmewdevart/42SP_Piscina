char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[j] != '\0')
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}

#include <stdio.h>
char    ft_strcat(char *dest, char *src);

int main()
{
    char d[] = "Ana";
    char s[]  = " Maria";
    ft_strcat(d, s);
    printf("%s", d);
    return 0;
}
